% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
handles.B=get(handles.popupmenu1,'value');
out=zeros(size(handles.a));
switch handles.B
    case 1
        arg=getBI(handles);
        if any(isnan(arg))
            error('Not Valid');
        else
            out=bilateral3D_alternativo(handles.a,arg(5),[arg(1) arg(2)],[arg(3) arg(4)],1);
        end
    case 2
        arg =str2double(get(handles.edit18,'String'));
        if (isnan(arg))
            error('Not Valid');
        else
            pb = waitbar(0,'1','Name','Processing binomial filter');  
            for i=1:size(handles.a,3)
                waitbar(i/size(handles.a,3),pb,'Doing binomial filter');
                if(get(handles.radiobutton1,'Value'))
                [out(:,:,i),~]=binomf(handles.a(:,:,i),arg);
                else
                [~,out(:,:,i)]=binomf(handles.a(:,:,i),arg);
                end
            end
            close(pb);
        end
end
imshow(out(:,:,1),[]);
handles.salida=out;
guidata(hObject, handles);



