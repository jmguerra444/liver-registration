% --- Executes on selection change in popupmenu1.
function popupmenu1_Callback(hObject, eventdata, handles)
handles.B=get(handles.popupmenu1,'value');
%disp(handles.B);
switch handles.B
    case 1
        set(handles.uipanel2,'Visible','On');
        set(handles.uipanel3,'Visible','Off');
